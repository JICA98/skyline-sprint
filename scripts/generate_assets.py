def generate_font_tga():
    # 96 characters from ASCII 32 to 127
    # Arrange them in a grid of 16 columns x 6 rows (each char is 8x8 pixels)
    # Total width = 16 * 8 = 128 pixels, height = 6 * 8 = 48 pixels
    width = 128
    height = 48
    pixels = []

    for y in range(height):
        row_idx = y // 8
        pixel_y = y % 8
        for x in range(width):
            col_idx = x // 8
            pixel_x = x % 8
            char_code = 32 + (row_idx * 16) + col_idx
            char = chr(char_code) if 32 <= char_code < 128 else ' '
            
            # Check if pixel is set
            active = False
            if char in font_glyphs:
                pattern = font_glyphs[char]
                if pattern[pixel_y] & (1 << (7 - pixel_x)):
                    active = True
            
            if active:
                pixels.append((248, 250, 252, 255)) # Main text #F8FAFC
            else:
                pixels.append((0, 0, 0, 0)) # Transparent
                
    save_tga("package/assets/images/font.tga", width, height, pixels)

def generate_player_tga():
    # 32x32 player courier (Pulse)
    # Circular body, cyan visor line (#22D3EE), two short legs
    width = 32
    height = 32
    pixels = []
    
    for y in range(height):
        for x in range(width):
            # Calculate distance from center (16, 15) for rounded body
            dx = x - 16
            dy = y - 14
            dist = math.sqrt(dx*dx + dy*dy)
            
            # Draw legs at the bottom
            if 24 <= y <= 28 and (10 <= x <= 12 or 19 <= x <= 21):
                pixels.append((148, 163, 184, 255)) # Leg color (#94A3B8)
            # Draw visor line
            elif 10 <= y <= 12 and 8 <= x <= 24:
                pixels.append((34, 211, 238, 255)) # Cyan Visor (#22D3EE)
            # Draw body
            elif dist <= 9:
                pixels.append((31, 41, 55, 255)) # Dark body (#1F2937)
            else:
                pixels.append((0, 0, 0, 0))
                
    save_tga("package/assets/images/player.tga", width, height, pixels)

def generate_enemy_tga():
    # 32x32 drone (triangular/hexagonal design with secondary accent #A78BFA)
    width = 32
    height = 32
    pixels = []
    
    for y in range(height):
        for x in range(width):
            # Draw a hexagonal/triangular shape
            # Center at (16, 16)
            dx = abs(x - 16)
            dy = abs(y - 16)
            
            # Simple hex outline logic
            if (dx + dy * 0.58) <= 12:
                # Inner eye
                if dx <= 2 and dy <= 2:
                    pixels.append((251, 113, 133, 255)) # Warning/Failure red (#FB7185)
                else:
                    pixels.append((167, 139, 250, 255)) # Purple (#A78BFA)
            else:
                pixels.append((0, 0, 0, 0))
                
    save_tga("package/assets/images/enemy.tga", width, height, pixels)

