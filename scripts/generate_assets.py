# Sound effects generation (WAV PCM 16-bit Mono 22050Hz)
# ----------------------------------------------------------------------------
def generate_audio():
    sample_rate = 22050
    
    # 1. Jump sound: upward sine sweep (from 220 Hz to 660 Hz) over 0.15s
    duration = 0.15
    num_samples = int(sample_rate * duration)
    jump_samples = []
    for i in range(num_samples):
        t = i / sample_rate
        # Linear frequency interpolation
        freq = 220 + (660 - 220) * (t / duration)
        phase = 2 * math.pi * freq * t
        val = math.sin(phase)
        # Fade out envelope
        envelope = 1.0 - (t / duration)
        jump_samples.append(val * envelope * 0.5)
    save_wav("package/assets/audio/jump.wav", sample_rate, jump_samples)
    
    # 2. Pickup sound: double-tone chime (500Hz for 0.05s, 800Hz for 0.1s)
    pickup_samples = []
    # First tone
    num_s1 = int(sample_rate * 0.05)
    for i in range(num_s1):
        t = i / sample_rate
        val = math.sin(2 * math.pi * 500 * t)
        pickup_samples.append(val * 0.4)
    # Second tone
    num_s2 = int(sample_rate * 0.10)
    for i in range(num_s2):
        t = i / sample_rate
        val = math.sin(2 * math.pi * 800 * t)
        # Fade out
        envelope = 1.0 - (t / 0.10)
        pickup_samples.append(val * envelope * 0.4)
    save_wav("package/assets/audio/pickup.wav", sample_rate, pickup_samples)
    
    # 3. Failure sound: downward noise sweep (from 350Hz to 60Hz) over 0.4s
    duration = 0.4
    num_samples = int(sample_rate * duration)
    failure_samples = []
    # Simple LCG noise generator
    noise_state = 1
    for i in range(num_samples):
        t = i / sample_rate
        freq = 350 - (350 - 60) * (t / duration)
        phase = 2 * math.pi * freq * t
        # Pure tone
        tone = math.sin(phase)
        # Low frequency rumble + noise mix
        noise_state = (noise_state * 1103515245 + 12345) & 0x7FFFFFFF
        noise_val = ((noise_state / 0x7FFFFFFF) * 2.0 - 1.0)
        
        # Mix tone and noise
        val = tone * 0.6 + noise_val * 0.2
        envelope = 1.0 - (t / duration)
        failure_samples.append(val * envelope * 0.5)
    save_wav("package/assets/audio/failure.wav", sample_rate, failure_samples)

